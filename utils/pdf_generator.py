from fpdf import FPDF
import os
import qrcode

class PDF(FPDF):
    def header(self):
        if os.path.exists("static/logo.png"):
            self.image("static/logo.png", 10, 8, 30)
        self.set_font("Helvetica", "B", 16)
        self.cell(0, 10, "Navadharma Prediction Report", ln=True, align="C")
        self.ln(10)

    def footer(self):
        self.set_y(-20)
        self.set_font("Helvetica", "I", 10)
        self.cell(0, 10, "â€” Nav, Principal Astrologer", ln=True, align="C")
        self.cell(0, 10, "Generated by Navadharma.com", align="C")

def generate_pdf(data: dict, filename="report.pdf"):
    pdf = PDF()
    pdf.add_page()
    pdf.set_auto_page_break(auto=True, margin=15)

    def section_title(title):
        pdf.set_font("Helvetica", "B", 13)
        pdf.set_text_color(0)
        pdf.cell(0, 10, title, ln=True)
        pdf.set_font("Helvetica", "", 12)
        pdf.set_text_color(30, 30, 30)

    # Birth Details
    section_title("ðŸ“… Birth Details")
    pdf.cell(0, 10, f"Date: {data.get('date', 'â€”')}", ln=True)
    pdf.cell(0, 10, f"Time: {data.get('time', 'â€”')}", ln=True)
    pdf.cell(0, 10, f"Place: {data.get('place', 'â€”')}", ln=True)
    pdf.ln(5)

    # Charts
    for chart, label in [("birth_chart.png", "ðŸŒ€ Birth Chart"), ("kp_chart.png", "ðŸ§­ KP Chart")]:
        chart_path = f"static/{chart}"
        if os.path.exists(chart_path):
            section_title(label)
            pdf.image(chart_path, w=100)
            pdf.ln(5)

    # Lagna & Dasha
    section_title("ðŸŒ  Astrological Snapshot")
    lagna = data.get("lagna", "â€”")
    dasha = data.get("currentDasha", {})
    pdf.cell(0, 10, f"Lagna: {lagna}", ln=True)
    pdf.cell(0, 10, f"Mahadasha: {dasha.get('mahadasha', 'â€”')}, Antardasha: {dasha.get('antardasha', 'â€”')}", ln=True)
    pdf.cell(0, 10, f"Dasha Period: {dasha.get('period', 'â€”')}", ln=True)
    pdf.ln(5)

    # Predictions
    section_title("ðŸ”® Predictions")
    for topic, content in data.get("predictions", {}).items():
        pdf.set_font("Helvetica", "B", 12)
        pdf.cell(0, 8, topic.capitalize(), ln=True)
        pdf.set_font("Helvetica", "", 12)

        if isinstance(content, dict):
            for k, v in content.items():
                if k.lower() != "hidden":
                    pdf.multi_cell(0, 8, f"{k.capitalize()}: {v}")
        if content.get("hidden", False):
            pdf.set_text_color(150, 0, 0)
            pdf.cell(0, 8, "ðŸ”’ Full reading available in Premium Report", ln=True)
            pdf.set_text_color(0)
        pdf.ln(4)

    # Yogas
    yogas = data.get("yogas", [])
    if yogas:
        section_title("ðŸ§˜ Significant Yogas")
        for yoga in yogas:
            pdf.cell(0, 8, f"â€¢ {yoga}", ln=True)
        pdf.ln(4)

    # Remedies
    remedies = data.get("remedies", [])
    if remedies:
        section_title("ðŸ•‰ Remedies Suggested")
        for r in remedies:
            pdf.cell(0, 8, f"âœ“ {r}", ln=True)
        pdf.ln(4)

    # QR Code
    qr_data = "https://navadharma.com"
    qr_path = "static/nav_qr.png"
    qrcode.make(qr_data).save(qr_path)
    pdf.image(qr_path, x=160, y=pdf.get_y(), w=30)

    output_path = f"static/{filename}"
    pdf.output(output_path, "F")
    return output_path

